class calcu:
    def __init__(self,a,b):
        self.a = a
        self.b = b
        
    def sum(self):
        print('Hasil dari {} + {} adalah {}'.format(self.a,self.b, self.a + self.b))

    def decrease(self):
        print('Hasil dari {} + {} adalah {}'.format(self.a,self.b, self.a - self.b))

    def division(self):
        print('Hasil dari {} + {} adalah {}'.format(self.a,self.b, self.a / self.b))

    def multiplication(self):
        print('Hasil dari {} + {} adalah {}'.format(self.a,self.b, self.a * self.b))


